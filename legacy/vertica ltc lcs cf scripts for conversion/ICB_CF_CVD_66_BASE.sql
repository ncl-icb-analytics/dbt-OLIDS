FROM CERNER_MEDICATION_JOINED_TABLE
WHERE DRUG_CODE IN (
                    SELECT SNOMED_CODE
                    FROM JOINED_LTC_LOOKUP
                    WHERE CLUSTER_ID = 'STAT_COD'
                    )
AND START_DT_TM >= ADD_MONTHS(CURRENT_DATE(),-12)

EXCEPT

