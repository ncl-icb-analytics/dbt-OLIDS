FROM NCL_CODES
WHERE CODE IN (
                    SELECT SNOMED_CODE
                    FROM JOINED_LTC_LOOKUP
                    WHERE CLUSTER_ID IN ('STATIN_ALLERGY_ADVERSE_REACTION', 'STATIN_NOT_INDICATED')
                    )

